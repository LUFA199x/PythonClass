# INHERITANCE IN PYTHON

# Inheritance is when we can create attributes and functions inside of a class and basically create another class to inherit those functions and attributes, So we can have a class that has all the functionality of another class without haing to write all those attributes

from chef import chef
from ChineseChef import ChineseChef

myChef = chef()
myChef.make_chicken()
myChef.make_salad()
myChef.make_special_dish()

myChineseChef = ChineseChef()
myChineseChef.make_chicken()
myChineseChef.make_special_dish()


